// While Loop
let index = 0;
while (index<=10) {
    // console.log(`Value: ${index}`);
    index = index + 2
}

let myArray = ["Naruto","Eren","Tanjiro"]

let arr = 0;
while (arr<myArray.length) {
    // console.log(`Value is: ${myArray[arr]}`);
    arr = arr + 1
}

// Do while loop

let score = 11;

do {
    console.log(`Score is: ${score}`);
    score++
} while (score<=10);